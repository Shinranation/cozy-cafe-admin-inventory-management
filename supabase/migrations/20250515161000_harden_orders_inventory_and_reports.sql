CREATE OR REPLACE FUNCTION public.activity_log_table_change()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_old jsonb := NULL;
  v_new jsonb := NULL;
  v_row jsonb;
  v_area text := 'General';
  v_action text := lower(TG_OP);
  v_entity_type text := TG_TABLE_NAME;
  v_entity_id text;
  v_entity_name text;
  v_description text;
  v_metadata jsonb;
  v_changed_field text;
  v_menu_name text;
  v_ingredient_name text;
  v_quantity text;
  v_unit text;
BEGIN
  IF TG_TABLE_NAME IN ('audit_logs', 'activity_logs') THEN
    IF TG_OP = 'DELETE' THEN
      RETURN OLD;
    END IF;
    RETURN NEW;
  END IF;

  IF TG_OP = 'INSERT' THEN
    v_new := to_jsonb(NEW);
    v_row := v_new;
  ELSIF TG_OP = 'UPDATE' THEN
    v_old := to_jsonb(OLD);
    v_new := to_jsonb(NEW);
    v_row := v_new;

    IF v_old = v_new THEN
      RETURN NEW;
    END IF;
  ELSE
    v_old := to_jsonb(OLD);
    v_row := v_old;
  END IF;

  v_metadata := jsonb_build_object(
    'table_name', TG_TABLE_NAME,
    'operation', TG_OP,
    'old_data', v_old,
    'new_data', v_new
  );

  CASE TG_TABLE_NAME
    WHEN 'inventory' THEN
      v_area := 'Inventory';
      v_entity_type := 'Ingredient';
      v_entity_id := v_row ->> 'ingredient_id';
      v_entity_name := coalesce(v_row ->> 'name', 'Ingredient #' || coalesce(v_entity_id, '?'));

      IF TG_OP = 'INSERT' THEN
        v_action := 'create';
        v_description := format(
          'Added ingredient %s with %s %s on hand.',
          v_entity_name,
          public.activity_log_number(v_new -> 'current_quantity'),
          coalesce(v_new ->> 'unit_of_measure', 'unit')
        );
      ELSIF TG_OP = 'DELETE' THEN
        v_action := 'delete';
        v_description := format('Deleted ingredient %s.', v_entity_name);
      ELSE
        IF (v_old -> 'current_quantity') IS DISTINCT FROM (v_new -> 'current_quantity') THEN
          v_action := 'stock_update';
          v_description := format(
            '%s stock changed from %s %s to %s %s.',
            v_entity_name,
            public.activity_log_number(v_old -> 'current_quantity'),
            coalesce(v_old ->> 'unit_of_measure', 'unit'),
            public.activity_log_number(v_new -> 'current_quantity'),
            coalesce(v_new ->> 'unit_of_measure', 'unit')
          );
        ELSIF (v_old -> 'is_active') IS DISTINCT FROM (v_new -> 'is_active') THEN
          v_action := CASE WHEN coalesce((v_new ->> 'is_active')::boolean, true) THEN 'restore' ELSE 'archive' END;
          v_description := CASE
            WHEN coalesce((v_new ->> 'is_active')::boolean, true) THEN format('Restored ingredient %s.', v_entity_name)
            ELSE format('Archived ingredient %s.', v_entity_name)
          END;
        ELSE
          v_changed_field := public.activity_log_first_changed_field(
            v_old,
            v_new,
            ARRAY['name', 'classification', 'unit_of_measure', 'low_stock']
          );
          v_action := 'update';
          v_description := CASE v_changed_field
            WHEN 'name' THEN format('Renamed ingredient from %s to %s.', v_old ->> 'name', v_new ->> 'name')
            WHEN 'classification' THEN format('Changed %s type from %s to %s.', v_entity_name, coalesce(v_old ->> 'classification', 'blank'), coalesce(v_new ->> 'classification', 'blank'))
            WHEN 'unit_of_measure' THEN format('Changed %s unit from %s to %s.', v_entity_name, coalesce(v_old ->> 'unit_of_measure', 'blank'), coalesce(v_new ->> 'unit_of_measure', 'blank'))
            WHEN 'low_stock' THEN format('Changed %s low-stock alert from %s to %s.', v_entity_name, public.activity_log_number(v_old -> 'low_stock'), public.activity_log_number(v_new -> 'low_stock'))
            ELSE format('Updated ingredient %s.', v_entity_name)
          END;
        END IF;
      END IF;

    WHEN 'inventory_transactions' THEN
      v_area := 'Inventory';
      v_entity_type := 'Stock Movement';
      v_entity_id := v_row ->> 'transaction_id';
      v_action := coalesce(v_row ->> 'transaction_type', lower(TG_OP));
      v_quantity := public.activity_log_number(v_row -> 'quantity_change');

      SELECT i.name, i.unit_of_measure
      INTO v_ingredient_name, v_unit
      FROM public.inventory i
      WHERE i.ingredient_id = (v_row ->> 'ingredient_id')::bigint;

      v_entity_name := coalesce(v_ingredient_name, 'Ingredient #' || coalesce(v_row ->> 'ingredient_id', '?'));

      IF TG_OP = 'INSERT' THEN
        IF v_action = 'sale' THEN
          v_description := format('Deducted %s %s from %s for order #%s.', v_quantity, coalesce(v_unit, 'unit'), v_entity_name, coalesce(v_row ->> 'reference_id', '?'));
        ELSIF v_action = 'void' THEN
          v_description := format('Restored %s %s to %s for voided order #%s.', v_quantity, coalesce(v_unit, 'unit'), v_entity_name, coalesce(v_row ->> 'reference_id', '?'));
        ELSIF v_action = 'stock_in' THEN
          v_description := format('Added %s %s to %s.', v_quantity, coalesce(v_unit, 'unit'), v_entity_name);
        ELSIF v_action = 'stock_out' THEN
          v_description := format('Removed %s %s from %s.', replace(v_quantity, '-', ''), coalesce(v_unit, 'unit'), v_entity_name);
        ELSE
          v_description := format('Recorded %s movement for %s: %s %s.', v_action, v_entity_name, v_quantity, coalesce(v_unit, 'unit'));
        END IF;

        IF nullif(trim(coalesce(v_row ->> 'reason', '')), '') IS NOT NULL THEN
          v_description := v_description || ' Reason: ' || (v_row ->> 'reason');
        END IF;
      ELSE
        v_description := format('%s stock movement for %s.', initcap(lower(TG_OP)), v_entity_name);
      END IF;

    WHEN 'menu' THEN
      v_area := 'Menu';
      v_entity_type := 'Menu Item';
      v_entity_id := v_row ->> 'item_id';
      v_entity_name := coalesce(v_row ->> 'name', 'Menu item #' || coalesce(v_entity_id, '?'));

      IF TG_OP = 'INSERT' THEN
        v_action := 'create';
        v_description := format('Added menu item %s for %s.', v_entity_name, public.activity_log_money(v_new -> 'price'));
      ELSIF TG_OP = 'DELETE' THEN
        v_action := 'delete';
        v_description := format('Deleted menu item %s.', v_entity_name);
      ELSE
        IF (v_old -> 'price') IS DISTINCT FROM (v_new -> 'price') THEN
          v_action := 'price_update';
          v_description := format('%s price changed from %s to %s.', v_entity_name, public.activity_log_money(v_old -> 'price'), public.activity_log_money(v_new -> 'price'));
        ELSIF (v_old -> 'availability_status') IS DISTINCT FROM (v_new -> 'availability_status') THEN
          v_action := 'availability_update';
          v_description := format('%s availability changed from %s to %s.', v_entity_name, coalesce(v_old ->> 'availability_status', 'blank'), coalesce(v_new ->> 'availability_status', 'blank'));
        ELSE
          v_changed_field := public.activity_log_first_changed_field(
            v_old,
            v_new,
            ARRAY['name', 'size_label', 'category', 'description', 'image_url']
          );
          v_action := 'update';
          v_description := CASE v_changed_field
            WHEN 'name' THEN format('Renamed menu item from %s to %s.', v_old ->> 'name', v_new ->> 'name')
            WHEN 'size_label' THEN format('Changed %s size from %s to %s.', v_entity_name, coalesce(v_old ->> 'size_label', 'blank'), coalesce(v_new ->> 'size_label', 'blank'))
            WHEN 'category' THEN format('Moved %s from %s to %s.', v_entity_name, coalesce(v_old ->> 'category', 'blank'), coalesce(v_new ->> 'category', 'blank'))
            WHEN 'image_url' THEN format('Updated photo for %s.', v_entity_name)
            ELSE format('Updated menu item %s.', v_entity_name)
          END;
        END IF;
      END IF;

    WHEN 'menu_ingredients' THEN
      v_area := 'Menu';
      v_entity_type := 'Recipe Ingredient';
      v_entity_id := v_row ->> 'menu_ingredient_id';

      SELECT m.name
      INTO v_menu_name
      FROM public.menu m
      WHERE m.item_id = (v_row ->> 'menu_item_id')::bigint;

      SELECT i.name, i.unit_of_measure
      INTO v_ingredient_name, v_unit
      FROM public.inventory i
      WHERE i.ingredient_id = (v_row ->> 'ingredient_id')::bigint;

      v_entity_name := coalesce(v_menu_name, 'Menu item #' || coalesce(v_row ->> 'menu_item_id', '?'));

      IF TG_OP = 'INSERT' THEN
        v_action := 'recipe_add';
        v_description := format(
          'Linked %s to %s recipe: %s %s per item.',
          coalesce(v_ingredient_name, 'ingredient #' || coalesce(v_row ->> 'ingredient_id', '?')),
          v_entity_name,
          public.activity_log_number(v_new -> 'quantity_required'),
          coalesce(v_new ->> 'unit_of_measure', v_unit, 'unit')
        );
      ELSIF TG_OP = 'DELETE' THEN
        v_action := 'recipe_remove';
        v_description := format('Removed %s from %s recipe.', coalesce(v_ingredient_name, 'ingredient'), v_entity_name);
      ELSE
        v_action := 'recipe_update';
        v_description := format(
          'Updated %s recipe ingredient %s from %s %s to %s %s.',
          v_entity_name,
          coalesce(v_ingredient_name, 'ingredient'),
          public.activity_log_number(v_old -> 'quantity_required'),
          coalesce(v_old ->> 'unit_of_measure', v_unit, 'unit'),
          public.activity_log_number(v_new -> 'quantity_required'),
          coalesce(v_new ->> 'unit_of_measure', v_unit, 'unit')
        );
      END IF;

    WHEN 'orders' THEN
      v_area := 'Revenue';
      v_entity_type := 'Order';
      v_entity_id := v_row ->> 'order_id';
      v_entity_name := 'Order #' || coalesce(v_entity_id, '?');

      IF TG_OP = 'INSERT' THEN
        v_action := 'order_create';
        v_description := format('Created pending order #%s for %s.', coalesce(v_entity_id, '?'), public.activity_log_money(v_new -> 'total_amount'));
      ELSIF TG_OP = 'DELETE' THEN
        v_action := 'order_delete';
        v_description := format('Deleted order #%s.', coalesce(v_entity_id, '?'));
      ELSIF (v_old -> 'status') IS DISTINCT FROM (v_new -> 'status') THEN
        v_action := CASE v_new ->> 'status'
          WHEN 'received' THEN 'order_received'
          WHEN 'voided' THEN 'order_voided'
          ELSE 'order_status_update'
        END;
        v_description := CASE v_new ->> 'status'
          WHEN 'received' THEN format('Marked order #%s as received.', coalesce(v_entity_id, '?'))
          WHEN 'voided' THEN format('Voided order #%s. Reason: %s', coalesce(v_entity_id, '?'), coalesce(nullif(v_new ->> 'void_reason', ''), 'No reason provided'))
          ELSE format('Changed order #%s status from %s to %s.', coalesce(v_entity_id, '?'), coalesce(v_old ->> 'status', 'blank'), coalesce(v_new ->> 'status', 'blank'))
        END;
      ELSE
        v_action := 'order_update';
        v_description := format('Updated order #%s.', coalesce(v_entity_id, '?'));
      END IF;

    WHEN 'payments' THEN
      v_area := 'Revenue';
      v_entity_type := 'Payment';
      v_entity_id := v_row ->> 'payment_id';
      v_entity_name := 'Payment #' || coalesce(v_entity_id, '?');

      IF TG_OP = 'INSERT' THEN
        v_action := 'payment_recorded';
        v_description := format(
          'Recorded %s payment of %s for order #%s.',
          coalesce(v_new ->> 'payment_method', 'payment'),
          public.activity_log_money(v_new -> 'amount'),
          coalesce(v_new ->> 'order_id', '?')
        );
      ELSIF TG_OP = 'DELETE' THEN
        v_action := 'payment_delete';
        v_description := format('Deleted payment #%s for order #%s.', coalesce(v_entity_id, '?'), coalesce(v_old ->> 'order_id', '?'));
      ELSE
        v_action := 'payment_update';
        v_description := format('Updated payment #%s.', coalesce(v_entity_id, '?'));
      END IF;

    WHEN 'expenses' THEN
      v_area := 'Revenue';
      v_entity_type := 'Expense';
      v_entity_id := v_row ->> 'expense_id';
      v_entity_name := coalesce(v_row ->> 'expense_name', 'Expense #' || coalesce(v_entity_id, '?'));

      IF TG_OP = 'INSERT' THEN
        v_action := 'expense_create';
        v_description := format('Recorded expense %s for %s.', v_entity_name, public.activity_log_money(v_new -> 'amount'));
      ELSIF TG_OP = 'DELETE' THEN
        v_action := 'expense_delete';
        v_description := format('Deleted expense %s.', v_entity_name);
      ELSIF (v_old -> 'amount') IS DISTINCT FROM (v_new -> 'amount') THEN
        v_action := 'expense_update';
        v_description := format('%s amount changed from %s to %s.', v_entity_name, public.activity_log_money(v_old -> 'amount'), public.activity_log_money(v_new -> 'amount'));
      ELSE
        v_action := 'expense_update';
        v_description := format('Updated expense %s.', v_entity_name);
      END IF;

    WHEN 'order_items' THEN
      v_area := 'Revenue';
      v_entity_type := 'Order Item';
      v_entity_id := v_row ->> 'order_item_id';
      v_entity_name := coalesce(v_row ->> 'item_name_snapshot', 'Menu item #' || coalesce(v_row ->> 'menu_item_id', '?'));

      IF TG_OP = 'INSERT' THEN
        v_action := 'order_item_add';
        v_description := format(
          'Added %s x%s to order #%s.',
          v_entity_name,
          coalesce(v_new ->> 'quantity', '?'),
          coalesce(v_new ->> 'order_id', '?')
        );
      ELSIF TG_OP = 'DELETE' THEN
        v_action := 'order_item_delete';
        v_description := format('Removed %s from order #%s.', v_entity_name, coalesce(v_old ->> 'order_id', '?'));
      ELSE
        v_action := 'order_item_update';
        v_description := format('Updated %s in order #%s.', v_entity_name, coalesce(v_new ->> 'order_id', '?'));
      END IF;

    ELSE
      v_area := 'General';
      v_action := lower(TG_OP);
      v_entity_type := TG_TABLE_NAME;
      v_description := format('%s on %s.', initcap(lower(TG_OP)), TG_TABLE_NAME);
  END CASE;

  INSERT INTO public.activity_logs (
    actor_id,
    actor_email,
    area,
    action,
    entity_type,
    entity_id,
    entity_name,
    description,
    metadata
  )
  VALUES (
    auth.uid(),
    coalesce(auth.jwt() ->> 'email', 'system'),
    v_area,
    v_action,
    v_entity_type,
    v_entity_id,
    v_entity_name,
    v_description,
    v_metadata
  );

  IF TG_OP = 'DELETE' THEN
    RETURN OLD;
  END IF;

  RETURN NEW;
END;
$$;

