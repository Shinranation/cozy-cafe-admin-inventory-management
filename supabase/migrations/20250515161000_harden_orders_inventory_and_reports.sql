CREATE TABLE IF NOT EXISTS public.audit_logs (
  log_id bigint GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  created_at timestamp with time zone NOT NULL DEFAULT now(),
  actor_id uuid,
  actor_email text,
  action text NOT NULL CHECK (action IN ('INSERT', 'UPDATE', 'DELETE')),
  schema_name text NOT NULL DEFAULT 'public',
  table_name text NOT NULL,
  record_pk jsonb NOT NULL DEFAULT '{}'::jsonb,
  summary text,
  old_data jsonb,
  new_data jsonb
);

CREATE INDEX IF NOT EXISTS audit_logs_created_at_idx
  ON public.audit_logs (created_at DESC);

CREATE INDEX IF NOT EXISTS audit_logs_table_action_idx
  ON public.audit_logs (table_name, action, created_at DESC);

CREATE OR REPLACE FUNCTION public.audit_record_pk(p_table_name text, p_row jsonb)
RETURNS jsonb
LANGUAGE plpgsql
IMMUTABLE
AS $$
BEGIN
  CASE p_table_name
    WHEN 'inventory' THEN
      RETURN jsonb_build_object('ingredient_id', p_row -> 'ingredient_id');
    WHEN 'inventory_transactions' THEN
      RETURN jsonb_build_object('transaction_id', p_row -> 'transaction_id');
    WHEN 'menu' THEN
      RETURN jsonb_build_object('item_id', p_row -> 'item_id');
    WHEN 'menu_ingredients' THEN
      RETURN jsonb_build_object('menu_ingredient_id', p_row -> 'menu_ingredient_id');
    WHEN 'orders' THEN
      RETURN jsonb_build_object('order_id', p_row -> 'order_id');
    WHEN 'order_items' THEN
      RETURN jsonb_build_object('order_item_id', p_row -> 'order_item_id');
    WHEN 'payments' THEN
      RETURN jsonb_build_object('payment_id', p_row -> 'payment_id');
    WHEN 'expenses' THEN
      RETURN jsonb_build_object('expense_id', p_row -> 'expense_id');
    ELSE
      RETURN '{}'::jsonb;
  END CASE;
END;
$$;

CREATE OR REPLACE FUNCTION public.audit_log_table_change()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_old jsonb;
  v_new jsonb;
  v_pk jsonb;
  v_summary text;
BEGIN
  IF TG_TABLE_NAME = 'audit_logs' THEN
    IF TG_OP = 'DELETE' THEN
      RETURN OLD;
    END IF;
    RETURN NEW;
  END IF;

  IF TG_OP = 'INSERT' THEN
    v_new := to_jsonb(NEW);
    v_pk := public.audit_record_pk(TG_TABLE_NAME, v_new);
    v_summary := format('%s inserted', TG_TABLE_NAME);
  ELSIF TG_OP = 'UPDATE' THEN
    v_old := to_jsonb(OLD);
    v_new := to_jsonb(NEW);
    v_pk := public.audit_record_pk(TG_TABLE_NAME, v_new);
    v_summary := format('%s updated', TG_TABLE_NAME);
  ELSE
    v_old := to_jsonb(OLD);
    v_pk := public.audit_record_pk(TG_TABLE_NAME, v_old);
    v_summary := format('%s deleted', TG_TABLE_NAME);
  END IF;

  INSERT INTO public.audit_logs (
    actor_id,
    actor_email,
    action,
    schema_name,
    table_name,
    record_pk,
    summary,
    old_data,
    new_data
  )
  VALUES (
    auth.uid(),
    coalesce(auth.jwt() ->> 'email', 'system'),
    TG_OP,
    TG_TABLE_SCHEMA,
    TG_TABLE_NAME,
    v_pk,
    v_summary,
    v_old,
    v_new
  );

  IF TG_OP = 'DELETE' THEN
    RETURN OLD;
  END IF;

  RETURN NEW;
END;
$$;

DO $$
DECLARE
  rec record;
BEGIN
  FOR rec IN
    SELECT *
    FROM (VALUES
      ('inventory'),
      ('inventory_transactions'),
      ('menu'),
      ('menu_ingredients'),
      ('orders'),
      ('order_items'),
      ('payments'),
      ('expenses')
    ) AS tracked(table_name)
  LOOP
    EXECUTE format('DROP TRIGGER IF EXISTS audit_%I_changes ON public.%I', rec.table_name, rec.table_name);
    EXECUTE format(
      'CREATE TRIGGER audit_%I_changes AFTER INSERT OR UPDATE OR DELETE ON public.%I FOR EACH ROW EXECUTE FUNCTION public.audit_log_table_change()',
