    SUM(oi.sub_total)::double precision AS gross_sales,
    COUNT(DISTINCT o.order_id)::bigint AS order_count,
    (SUM(oi.sub_total) / NULLIF(SUM(oi.quantity), 0))::double precision AS average_unit_price,
    MIN(o.created_at) AS first_sold_at,
    MAX(o.created_at) AS last_sold_at
  FROM public.orders o
  JOIN public.order_items oi ON oi.order_id = o.order_id
  LEFT JOIN public.menu m ON m.item_id = oi.menu_item_id
  WHERE o.status = 'received'
    AND o.created_at >= p_start_at
    AND o.created_at < p_end_at
  GROUP BY
    oi.menu_item_id,
    coalesce(oi.item_name_snapshot, m.name, 'Deleted Menu Item'),
    coalesce(oi.size_label_snapshot, m.size_label),
    coalesce(oi.category_snapshot, m.category, 'Uncategorized')
  ORDER BY
    SUM(oi.quantity) DESC,
    SUM(oi.sub_total) DESC,
    coalesce(oi.item_name_snapshot, m.name, 'Deleted Menu Item') ASC,
    coalesce(oi.size_label_snapshot, m.size_label) ASC;
