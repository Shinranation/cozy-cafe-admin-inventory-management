CREATE OR REPLACE FUNCTION public.list_sold_items_report(
  p_start_at timestamp with time zone,
  p_end_at timestamp with time zone
)
RETURNS TABLE (
  menu_item_id bigint,
  item_name text,
  size_label text,
  category text,
  quantity_sold bigint,
  gross_sales double precision,
  order_count bigint,
  average_unit_price double precision,
  first_sold_at timestamp with time zone,
  last_sold_at timestamp with time zone
