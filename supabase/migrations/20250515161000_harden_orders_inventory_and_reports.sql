-- The Cozzy Cup Cafe one-copy repair SQL.
-- Run this whole file once in Supabase SQL Editor after the base schema exists.
--
-- This is intentionally an upgrade/repair script, not a drop-and-recreate schema:
-- - Keeps existing rows.
-- - Makes reports snapshot-safe.
-- - Makes received orders the real completed sales.
-- - Restores inventory when received orders are voided.
-- - Keeps old RPC names as compatibility wrappers for the current website.

-- ============================================================================
-- 1) Schema hardening
-- ============================================================================

ALTER TABLE public.inventory
  ADD COLUMN IF NOT EXISTS is_active boolean NOT NULL DEFAULT true,
  ADD COLUMN IF NOT EXISTS classification text;

ALTER TABLE public.order_items
  ADD COLUMN IF NOT EXISTS item_name_snapshot text,
  ADD COLUMN IF NOT EXISTS size_label_snapshot text,
  ADD COLUMN IF NOT EXISTS category_snapshot text;

ALTER TABLE public.orders
  ADD COLUMN IF NOT EXISTS voided_at timestamp with time zone,
  ADD COLUMN IF NOT EXISTS void_reason text;

CREATE TABLE IF NOT EXISTS public.user_roles (
  user_id uuid PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
  role text NOT NULL CHECK (role IN ('admin', 'staff', 'customer')),
  created_at timestamp with time zone NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS public.menu_ingredients (
  menu_ingredient_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  menu_item_id bigint NOT NULL REFERENCES public.menu(item_id) ON DELETE CASCADE,
  ingredient_id bigint NOT NULL REFERENCES public.inventory(ingredient_id),
  quantity_required double precision NOT NULL CHECK (quantity_required > 0),
  unit_of_measure text NOT NULL DEFAULT 'unit',
  created_at timestamp with time zone NOT NULL DEFAULT now()
);

ALTER TABLE public.menu_ingredients
  ADD COLUMN IF NOT EXISTS unit_of_measure text NOT NULL DEFAULT 'unit',
  ADD COLUMN IF NOT EXISTS created_at timestamp with time zone NOT NULL DEFAULT now();

CREATE TABLE IF NOT EXISTS public.payments (
  payment_id bigint GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  order_id bigint NOT NULL REFERENCES public.orders(order_id),
  amount double precision NOT NULL DEFAULT 0 CHECK (amount >= 0),
  payment_method text NOT NULL DEFAULT 'cash',
  reference_number text,
  created_at timestamp with time zone NOT NULL DEFAULT now()
);

ALTER TABLE public.payments
  ADD COLUMN IF NOT EXISTS amount double precision NOT NULL DEFAULT 0,
  ADD COLUMN IF NOT EXISTS payment_method text NOT NULL DEFAULT 'cash',
  ADD COLUMN IF NOT EXISTS reference_number text,
  ADD COLUMN IF NOT EXISTS created_at timestamp with time zone NOT NULL DEFAULT now();

UPDATE public.orders
SET status = 'pending'
WHERE status IS NULL
  OR trim(status) = ''
  OR status NOT IN ('pending', 'received', 'voided');

ALTER TABLE public.orders
  DROP CONSTRAINT IF EXISTS orders_status_check;

ALTER TABLE public.orders
  ADD CONSTRAINT orders_status_check CHECK (status IN ('pending', 'received', 'voided'));

UPDATE public.order_items oi
SET
  item_name_snapshot = coalesce(oi.item_name_snapshot, m.name),
  size_label_snapshot = coalesce(oi.size_label_snapshot, m.size_label),
  category_snapshot = coalesce(oi.category_snapshot, m.category)
FROM public.menu m
WHERE m.item_id = oi.menu_item_id
  AND (
    oi.item_name_snapshot IS NULL
    OR oi.size_label_snapshot IS NULL
    OR oi.category_snapshot IS NULL
  );

CREATE INDEX IF NOT EXISTS orders_status_created_at_idx
  ON public.orders (status, created_at);

CREATE INDEX IF NOT EXISTS order_items_order_id_idx
  ON public.order_items (order_id);

CREATE INDEX IF NOT EXISTS order_items_menu_item_id_idx
  ON public.order_items (menu_item_id);

CREATE INDEX IF NOT EXISTS inventory_transactions_reference_type_idx
  ON public.inventory_transactions (reference_id, transaction_type);

