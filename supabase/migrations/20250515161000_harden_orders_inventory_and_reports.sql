GRANT EXECUTE ON FUNCTION public.is_admin_app_user() TO authenticated;

-- ============================================================================
-- 3) Confirm order: create a pending order only
--    No payment and no inventory deduction happen here.
--    The sale is completed by mark_order_received().
-- ============================================================================

CREATE OR REPLACE FUNCTION public.confirm_pos_order(
  p_cashier_id bigint,
  p_client_id bigint,
  p_guest_display_name text,
  p_lines jsonb
)
RETURNS bigint
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_order_id bigint;
  v_total double precision := 0;
  rec_stock record;
BEGIN
  IF NOT public.is_admin_app_user() THEN
    RAISE EXCEPTION 'confirm_pos_order: not authorized (admin role required)';
  END IF;

  IF p_lines IS NULL OR jsonb_typeof(p_lines) <> 'array' OR jsonb_array_length(p_lines) = 0 THEN
    RAISE EXCEPTION 'confirm_pos_order: p_lines must be a non-empty JSON array';
  END IF;

  IF p_cashier_id IS NULL THEN
    RAISE EXCEPTION 'confirm_pos_order: p_cashier_id is required';
  END IF;

  IF NOT EXISTS (SELECT 1 FROM public.cashier c WHERE c.cashier_id = p_cashier_id) THEN
    RAISE EXCEPTION 'confirm_pos_order: cashier_id % not found', p_cashier_id;
  END IF;

  IF p_client_id IS NOT NULL AND NOT EXISTS (SELECT 1 FROM public.clients cl WHERE cl.client_id = p_client_id) THEN
    RAISE EXCEPTION 'confirm_pos_order: client_id % not found', p_client_id;
  END IF;

  DROP TABLE IF EXISTS tmp_order_lines;
  DROP TABLE IF EXISTS tmp_menu_lines;
  DROP TABLE IF EXISTS tmp_ingredient_needs;

  CREATE TEMP TABLE tmp_order_lines ON COMMIT DROP AS
  WITH raw AS (
    SELECT
      (elem->>'menu_item_id')::bigint AS menu_item_id,
      (elem->>'quantity')::int AS quantity
    FROM jsonb_array_elements(p_lines) AS t(elem)
  ),
  cleaned AS (
    SELECT menu_item_id, quantity
    FROM raw
    WHERE menu_item_id IS NOT NULL AND quantity IS NOT NULL
  )
  SELECT menu_item_id, SUM(quantity)::int AS quantity
  FROM cleaned
  GROUP BY menu_item_id;

  IF NOT EXISTS (SELECT 1 FROM tmp_order_lines) THEN
    RAISE EXCEPTION 'confirm_pos_order: p_lines must include at least one valid menu item';
  END IF;

  IF EXISTS (SELECT 1 FROM tmp_order_lines WHERE quantity <= 0) THEN
    RAISE EXCEPTION 'confirm_pos_order: each quantity must be a positive integer';
  END IF;

  CREATE TEMP TABLE tmp_menu_lines ON COMMIT DROP AS
  SELECT
    t.menu_item_id,
    t.quantity,
    m.name,
    m.size_label,
    m.category,
    m.price,
    m.availability_status
  FROM tmp_order_lines t
  JOIN public.menu m ON m.item_id = t.menu_item_id;

  IF (SELECT count(*) FROM tmp_menu_lines) <> (SELECT count(*) FROM tmp_order_lines) THEN
    RAISE EXCEPTION 'confirm_pos_order: unknown menu_item_id in cart';
  END IF;

  IF EXISTS (
    SELECT 1
    FROM tmp_menu_lines ml
    WHERE lower(coalesce(ml.availability_status, '')) <> 'available'
  ) THEN
    RAISE EXCEPTION 'confirm_pos_order: one or more menu items are unavailable';
  END IF;

  CREATE TEMP TABLE tmp_ingredient_needs ON COMMIT DROP AS
  SELECT
    mi.ingredient_id,
    SUM(ml.quantity * mi.quantity_required)::double precision AS required_quantity,
    string_agg(ml.name || ' x' || ml.quantity, ', ' ORDER BY ml.name) AS menu_summary
  FROM tmp_menu_lines ml
  JOIN public.menu_ingredients mi ON mi.menu_item_id = ml.menu_item_id
  GROUP BY mi.ingredient_id;

  IF EXISTS (
    SELECT 1
    FROM tmp_ingredient_needs n
    JOIN public.inventory i ON i.ingredient_id = n.ingredient_id
    WHERE i.is_active = false
  ) THEN
    RAISE EXCEPTION 'ARCHIVED_INGREDIENT: one or more menu recipes use archived ingredients';
  END IF;

  FOR rec_stock IN
    SELECT
      i.ingredient_id,
      i.name,
      i.current_quantity,
      n.required_quantity,
      n.menu_summary
    FROM tmp_ingredient_needs n
    JOIN public.inventory i ON i.ingredient_id = n.ingredient_id
    WHERE i.is_active = true
    ORDER BY i.ingredient_id
    FOR UPDATE OF i
  LOOP
    IF rec_stock.current_quantity < rec_stock.required_quantity THEN
      RAISE EXCEPTION 'INSUFFICIENT_STOCK: % required %, available %. Menu: %.',
        rec_stock.name,
        rec_stock.required_quantity,
        floor(rec_stock.current_quantity),
        rec_stock.menu_summary;
    END IF;
  END LOOP;

  SELECT SUM(price * quantity)::double precision
  INTO v_total
  FROM tmp_menu_lines;

  INSERT INTO public.orders (cashier_id, client_id, total_amount, status, guest_display_name)
  VALUES (
    p_cashier_id,
    p_client_id,
    v_total,
    'pending',
    NULLIF(trim(coalesce(p_guest_display_name, '')), '')
  )
  RETURNING order_id INTO v_order_id;

  INSERT INTO public.order_items (
    order_id,
    menu_item_id,
    quantity,
    unit_price,
    sub_total,
    item_name_snapshot,
    size_label_snapshot,
    category_snapshot
  )
  SELECT
    v_order_id,
    menu_item_id,
    quantity,
    price,
    price * quantity,
    name,
    size_label,
    category
  FROM tmp_menu_lines;

  RETURN v_order_id;
END;
$$;

REVOKE ALL ON FUNCTION public.confirm_pos_order(bigint, bigint, text, jsonb) FROM PUBLIC;
GRANT EXECUTE ON FUNCTION public.confirm_pos_order(bigint, bigint, text, jsonb) TO authenticated;

-- ============================================================================
-- 4) Mark received: this is the completed sale
--    Deducts stock, writes sale transactions, and creates one cash payment.
--    Compatibility: if an older pending order already has sale transactions or
--    payment rows, it will not double-deduct or double-pay.
-- ============================================================================

CREATE OR REPLACE FUNCTION public.mark_order_received(p_order_id bigint)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_order public.orders%ROWTYPE;
  v_has_sale_transactions boolean := false;
  v_has_payment boolean := false;
  rec_stock record;
BEGIN
  IF NOT public.is_admin_app_user() THEN
    RAISE EXCEPTION 'mark_order_received: not authorized (admin role required)';
  END IF;

  SELECT *
  INTO v_order
  FROM public.orders
  WHERE order_id = p_order_id
    AND status = 'pending'
  FOR UPDATE;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'mark_order_received: order % not found or not pending', p_order_id;
  END IF;

  SELECT EXISTS (
    SELECT 1
    FROM public.inventory_transactions it
    WHERE it.reference_id = p_order_id
      AND it.transaction_type = 'sale'
      AND it.quantity_change < 0
  )
  INTO v_has_sale_transactions;

  SELECT EXISTS (
    SELECT 1
    FROM public.payments p
    WHERE p.order_id = p_order_id
  )
  INTO v_has_payment;

  DROP TABLE IF EXISTS tmp_receive_ingredient_needs;

  CREATE TEMP TABLE tmp_receive_ingredient_needs ON COMMIT DROP AS
  SELECT
    mi.ingredient_id,
    SUM(oi.quantity * mi.quantity_required)::double precision AS required_quantity,
    string_agg(
      coalesce(oi.item_name_snapshot, m.name, 'Deleted Menu Item') || ' x' || oi.quantity,
      ', '
      ORDER BY coalesce(oi.item_name_snapshot, m.name, 'Deleted Menu Item')
    ) AS menu_summary
  FROM public.order_items oi
  LEFT JOIN public.menu m ON m.item_id = oi.menu_item_id
  JOIN public.menu_ingredients mi ON mi.menu_item_id = oi.menu_item_id
  WHERE oi.order_id = p_order_id
  GROUP BY mi.ingredient_id;

  IF NOT v_has_sale_transactions THEN
    IF EXISTS (
      SELECT 1
      FROM tmp_receive_ingredient_needs n
      JOIN public.inventory i ON i.ingredient_id = n.ingredient_id
      WHERE i.is_active = false
    ) THEN
      RAISE EXCEPTION 'ARCHIVED_INGREDIENT: one or more menu recipes use archived ingredients';
    END IF;

    FOR rec_stock IN
      SELECT
        i.ingredient_id,
        i.name,
        i.current_quantity,
        n.required_quantity,
        n.menu_summary
      FROM tmp_receive_ingredient_needs n
      JOIN public.inventory i ON i.ingredient_id = n.ingredient_id
      WHERE i.is_active = true
      ORDER BY i.ingredient_id
      FOR UPDATE OF i
    LOOP
      IF rec_stock.current_quantity < rec_stock.required_quantity THEN
        RAISE EXCEPTION 'INSUFFICIENT_STOCK: % required %, available %. Menu: %.',
          rec_stock.name,
          rec_stock.required_quantity,
          floor(rec_stock.current_quantity),
          rec_stock.menu_summary;
      END IF;
    END LOOP;

    FOR rec_stock IN
      SELECT
        i.ingredient_id,
        i.name,
        n.required_quantity
      FROM tmp_receive_ingredient_needs n
      JOIN public.inventory i ON i.ingredient_id = n.ingredient_id
      WHERE i.is_active = true
      ORDER BY i.ingredient_id
    LOOP
      UPDATE public.inventory i
      SET current_quantity = i.current_quantity - rec_stock.required_quantity
      WHERE i.ingredient_id = rec_stock.ingredient_id;

      INSERT INTO public.inventory_transactions (
        ingredient_id,
        quantity_change,
        transaction_type,
        reference_id,
        reason,
        cashier_id
      )
      VALUES (
        rec_stock.ingredient_id,
        -rec_stock.required_quantity,
        'sale',
        p_order_id,
        format('Received order %s ingredient deduction: %s', p_order_id, rec_stock.name),
        v_order.cashier_id
      );
    END LOOP;
  END IF;

  IF NOT v_has_payment THEN
    INSERT INTO public.payments (order_id, amount, payment_method)
    VALUES (p_order_id, v_order.total_amount, 'cash');
  END IF;

  UPDATE public.orders
  SET status = 'received'
  WHERE order_id = p_order_id
    AND status = 'pending';
END;
$$;

REVOKE ALL ON FUNCTION public.mark_order_received(bigint) FROM PUBLIC;
GRANT EXECUTE ON FUNCTION public.mark_order_received(bigint) TO authenticated;

-- ============================================================================
-- 5) Cancel pending order
--    Useful for mistaken queue entries. It also repairs older pending orders that
--    already deducted inventory under the old confirm_pos_order function.
-- ============================================================================

CREATE OR REPLACE FUNCTION public.cancel_pending_order(
  p_order_id bigint,
  p_reason text DEFAULT 'Cancelled pending order'
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_order public.orders%ROWTYPE;
  v_restored_transactions bigint := 0;
  v_deleted_payments bigint := 0;
BEGIN
  IF NOT public.is_admin_app_user() THEN
    RAISE EXCEPTION 'cancel_pending_order: not authorized (admin role required)';
  END IF;

  SELECT *
  INTO v_order
  FROM public.orders
  WHERE order_id = p_order_id
    AND status = 'pending'
  FOR UPDATE;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'cancel_pending_order: order % not found or not pending', p_order_id;
  END IF;

  DROP TABLE IF EXISTS tmp_pending_inventory_reversals;

  CREATE TEMP TABLE tmp_pending_inventory_reversals ON COMMIT DROP AS
  SELECT
    it.ingredient_id,
    SUM(-it.quantity_change)::double precision AS quantity_restore
  FROM public.inventory_transactions it
  WHERE it.reference_id = p_order_id
    AND it.transaction_type = 'sale'
    AND it.quantity_change < 0
  GROUP BY it.ingredient_id;

  PERFORM 1
  FROM public.inventory i
  JOIN tmp_pending_inventory_reversals r ON r.ingredient_id = i.ingredient_id
  ORDER BY i.ingredient_id
  FOR UPDATE OF i;

  UPDATE public.inventory i
  SET current_quantity = i.current_quantity + r.quantity_restore
  FROM tmp_pending_inventory_reversals r
  WHERE r.ingredient_id = i.ingredient_id
    AND r.quantity_restore > 0;

  INSERT INTO public.inventory_transactions (
    ingredient_id,
    quantity_change,
    transaction_type,
    reference_id,
    reason,
    cashier_id
  )
  SELECT
    r.ingredient_id,
    r.quantity_restore,
    'void',
    p_order_id,
    format('Cancel pending order %s: restore sale deduction', p_order_id),
    v_order.cashier_id
  FROM tmp_pending_inventory_reversals r
  WHERE r.quantity_restore > 0;

  GET DIAGNOSTICS v_restored_transactions = ROW_COUNT;

  DELETE FROM public.payments
  WHERE order_id = p_order_id;

  GET DIAGNOSTICS v_deleted_payments = ROW_COUNT;

  UPDATE public.orders
  SET
    status = 'voided',
    voided_at = now(),
    void_reason = NULLIF(trim(coalesce(p_reason, '')), '')
  WHERE order_id = p_order_id
    AND status = 'pending';

  RETURN jsonb_build_object(
    'voided_order_id', p_order_id,
    'deleted_payments', v_deleted_payments,
    'restored_inventory_transactions', v_restored_transactions
  );
END;
$$;

REVOKE ALL ON FUNCTION public.cancel_pending_order(bigint, text) FROM PUBLIC;
GRANT EXECUTE ON FUNCTION public.cancel_pending_order(bigint, text) TO authenticated;

-- ============================================================================
-- 6) Inventory stock-in/stock-out RPCs
-- ============================================================================

CREATE OR REPLACE FUNCTION public.apply_inventory_stock_movement(
  p_ingredient_id bigint,
  p_quantity_change double precision,
  p_transaction_type text,
  p_reason text,
  p_reference_id bigint,
  p_cashier_id bigint,
  p_expense_amount double precision DEFAULT 0
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_inventory public.inventory%ROWTYPE;
  v_new_quantity double precision;
  v_transaction_id bigint;
  v_expense_id bigint;
BEGIN
  IF NOT public.is_admin_app_user() THEN
    RAISE EXCEPTION 'apply_inventory_stock_movement: not authorized (admin role required)';
  END IF;

  IF p_ingredient_id IS NULL THEN
    RAISE EXCEPTION 'apply_inventory_stock_movement: p_ingredient_id is required';
  END IF;

  IF p_quantity_change IS NULL OR p_quantity_change = 0 THEN
    RAISE EXCEPTION 'apply_inventory_stock_movement: quantity change must not be zero';
  END IF;

  IF p_transaction_type NOT IN ('stock_in', 'stock_out') THEN
    RAISE EXCEPTION 'apply_inventory_stock_movement: invalid transaction type';
  END IF;

  IF p_expense_amount IS NULL THEN
    p_expense_amount := 0;
  END IF;

  IF p_expense_amount < 0 THEN
    RAISE EXCEPTION 'apply_inventory_stock_movement: expense amount must be zero or higher';
  END IF;

  IF p_expense_amount > 0 AND p_quantity_change < 0 THEN
    RAISE EXCEPTION 'apply_inventory_stock_movement: expense amount is only valid for stock in';
  END IF;

  SELECT *
  INTO v_inventory
  FROM public.inventory
  WHERE ingredient_id = p_ingredient_id
  FOR UPDATE;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'apply_inventory_stock_movement: ingredient % not found', p_ingredient_id;
  END IF;

  IF v_inventory.is_active = false THEN
    RAISE EXCEPTION 'apply_inventory_stock_movement: ingredient % is archived', p_ingredient_id;
  END IF;

  v_new_quantity := v_inventory.current_quantity + p_quantity_change;

  IF v_new_quantity < 0 THEN
    RAISE EXCEPTION 'INSUFFICIENT_STOCK: % only has % %. Stock out cannot make inventory negative.',
      v_inventory.name,
      v_inventory.current_quantity,
      v_inventory.unit_of_measure;
  END IF;

  UPDATE public.inventory
  SET current_quantity = v_new_quantity
  WHERE ingredient_id = p_ingredient_id
  RETURNING * INTO v_inventory;

  INSERT INTO public.inventory_transactions (
    ingredient_id,
    quantity_change,
    transaction_type,
    reference_id,
    reason,
    cashier_id
  )
  VALUES (
    p_ingredient_id,
    p_quantity_change,
    p_transaction_type,
    p_reference_id,
    NULLIF(trim(coalesce(p_reason, '')), ''),
    p_cashier_id
  )
  RETURNING transaction_id INTO v_transaction_id;

  IF p_expense_amount > 0 THEN
    INSERT INTO public.expenses (
      expense_name,
      amount,
      category,
      cashier_id,
      notes
    )
    VALUES (
      format('Inventory stock in: %s', v_inventory.name),
      p_expense_amount,
      'Inventory',
      p_cashier_id,
      'Created from Inventory Stock In'
    )
    RETURNING expense_id INTO v_expense_id;
  END IF;

  RETURN jsonb_build_object(
    'inventory', to_jsonb(v_inventory),
    'transaction_id', v_transaction_id,
    'expense_id', v_expense_id
  );
END;
$$;

REVOKE ALL ON FUNCTION public.apply_inventory_stock_movement(bigint, double precision, text, text, bigint, bigint, double precision) FROM PUBLIC;
GRANT EXECUTE ON FUNCTION public.apply_inventory_stock_movement(bigint, double precision, text, text, bigint, bigint, double precision) TO authenticated;

CREATE OR REPLACE FUNCTION public.create_inventory_ingredient(
  p_name text,
  p_classification text,
  p_current_quantity double precision,
  p_unit_of_measure text,
  p_low_stock double precision,
  p_total_cost double precision,
  p_reference_id bigint,
  p_cashier_id bigint
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_inventory public.inventory%ROWTYPE;
  v_transaction_id bigint;
  v_expense_id bigint;
BEGIN
  IF NOT public.is_admin_app_user() THEN
    RAISE EXCEPTION 'create_inventory_ingredient: not authorized (admin role required)';
  END IF;

  p_name := NULLIF(trim(coalesce(p_name, '')), '');
  p_classification := NULLIF(trim(coalesce(p_classification, '')), '');
  p_unit_of_measure := NULLIF(trim(coalesce(p_unit_of_measure, '')), '');

  IF p_name IS NULL THEN
    RAISE EXCEPTION 'create_inventory_ingredient: name is required';
  END IF;

  IF p_unit_of_measure IS NULL THEN
    RAISE EXCEPTION 'create_inventory_ingredient: unit of measure is required';
  END IF;

  IF p_current_quantity IS NULL OR p_current_quantity < 0 THEN
    RAISE EXCEPTION 'create_inventory_ingredient: current quantity must be zero or higher';
  END IF;

  IF p_low_stock IS NULL OR p_low_stock < 0 THEN
    RAISE EXCEPTION 'create_inventory_ingredient: low stock must be zero or higher';
  END IF;

  IF p_total_cost IS NULL THEN
    p_total_cost := 0;
  END IF;

  IF p_total_cost < 0 THEN
    RAISE EXCEPTION 'create_inventory_ingredient: total cost must be zero or higher';
  END IF;

  INSERT INTO public.inventory (
    name,
    classification,
    current_quantity,
    unit_of_measure,
    low_stock
  )
  VALUES (
    p_name,
    p_classification,
    p_current_quantity,
    p_unit_of_measure,
    p_low_stock
  )
  RETURNING * INTO v_inventory;

  IF p_current_quantity > 0 THEN
    INSERT INTO public.inventory_transactions (
      ingredient_id,
      quantity_change,
      transaction_type,
      reference_id,
      reason,
      cashier_id
    )
    VALUES (
      v_inventory.ingredient_id,
      p_current_quantity,
      'stock_in',
      p_reference_id,
      'Initial ingredient entry',
      p_cashier_id
    )
    RETURNING transaction_id INTO v_transaction_id;
  END IF;

  IF p_total_cost > 0 THEN
    INSERT INTO public.expenses (
      expense_name,
      amount,
      category,
      cashier_id,
      notes
    )
    VALUES (
      format('Inventory stock in: %s', p_name),
      p_total_cost,
      'Inventory',
      p_cashier_id,
      'Created from Inventory Stock In'
    )
    RETURNING expense_id INTO v_expense_id;
  END IF;

  RETURN jsonb_build_object(
    'inventory', to_jsonb(v_inventory),
    'transaction_id', v_transaction_id,
    'expense_id', v_expense_id
  );
END;
$$;

