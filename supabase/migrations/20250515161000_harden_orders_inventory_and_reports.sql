LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  IF NOT public.is_admin_app_user() THEN
    RAISE EXCEPTION 'list_sold_items_report: not authorized (admin role required)';
